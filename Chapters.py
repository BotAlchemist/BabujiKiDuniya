def chapter_ten_commandments(i_lang):
    if i_lang=='en':
         page_context= '''
         
         1.     Arise before dawn. 
         Offer your prayer and puja ( worship) at the fixed hour,
         preferably before sunrise ,
         sitting in one and the same pose.
         Have a separate place and 'asan' (seat) for worship,
         where purity of mind and body be specially adhered to.
2.	Begin your puja with a prayer for spiritual elevation 
        with a heartful of love and devotion.
3.	Fix up your goal which should be 'Complete oneness with God'.
        Rest not till the ideal is achieved.
4.	Be plain and simple to be identical with Nature.
5.	Be truthful.Take miseries as divine blessings 
        for your own good and be thankful.
6.	Know all people as brethern and treat them as such.
7.	Be not revengeful for the wrongs done by others.
        Take them with gratitude as heavenly gifts.
8.	Be happy to eat in constant divine thoughts whatever you get ,
        with due regard  to honesty and pious earnings .
9.	Mould your living so as to rouse a feeling of love and pity in others .
10.	At bed time, feel the presence of God ,
        repent for the wrongs committed,
        beg forgiveness in a supplicant mood,
        resolving not to allow repetition of the same.

         
         '''
         
